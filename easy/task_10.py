#10. Перевірка подільності: зі списку чисел створіть новий список логічних
# значень, де кожен елемент вказує, чи ділиться відповідне число на
# даний дільник
a = list(map(int,input("Введіть цілі числа через пробіл: ").split( )))
y = int(input("Введіть ціле число: "))
new=[]
for i in a:
    new.append(bool(i%y==0))
print(new)
