#1. Фільтрувати за кількома умовами: числа, які діляться на X, але не
# діляться на Y.
a = list(map(int,input("Введіть цілі числа через пробіл: ").split( )))
x = int(input("Введіть ціле число, буде ділитись: "))
y = int(input("Введіть ціле число, не буде ділитись: "))
for i in a:
    if i%x==0 and i%y!=0:
        print(i, end=" ")
